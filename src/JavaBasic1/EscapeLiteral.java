package JavaBasic1;
public class EscapeLiteral {
	public static void main(String[] args) {
		// 제어문자(Escape Literal)
		System.out.print("가나다라마바사" + '\n');
		System.out.print("가나다라마바사" + '\n');
		System.out.println("가나\t다라\t마바사");
		System.out.print("가나다라마\r바사\n");
		System.out.print("가나다라마\"바사");
	}
}
